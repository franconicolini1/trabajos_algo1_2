  ok = abb_guardar(abb, "g", &datos[6]); 
  ok = abb_guardar(abb, "c", &datos[2]);
  ok = abb_guardar(abb, "a", &datos[0]);
  ok = abb_guardar(abb, "i", &datos[8]);
  ok = abb_guardar(abb, "e", &datos[4]);
  ok = abb_guardar(abb, "f", &datos[5]);
  ok = abb_guardar(abb, "b", &datos[1]);
  ok = abb_guardar(abb, "h", &datos[7]);
  ok = abb_guardar(abb, "d", &datos[3]);

  print_test("Se guardo correctamente", ok);

  void* extra = NULL;
  abb_in_order(abb, mult_por_dos, extra);

  for (size_t i = 0; i < 9; i++) {
    ok = (datos[i] == 2 * (i+1));
    if (!ok) break;
  }

  print_test("Se multiplicaron los elementos por 2", ok);

